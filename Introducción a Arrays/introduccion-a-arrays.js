// Arrays - Arreglos
// Un array o arreglo es una lista de caracteres, sean números, string o boleans

const tecnologias = [20, 30, true, 'NextJS', 'JS'] // Crear un array

console.table(tecnologias) // 'object'

console.log(tecnologias[3]) // Podemos inidicar el indice del array

console.log(tecnologias.length) // Usando length podemos ver el tamaño del array

console.log(tecnologias.toString()) // Usando toString() imprimimos todo el array en un string
// Condicionales if else else if

// Disponible
let num = 100000
// Retirar
let num2 = 20000

// La cantidad disponible es mayor a la cantidad que se quiere retirar, por lo tanto se podrá hacer el retiro, en caso contrario la cantidad supere al disponible, no se podrá retirar
if (num > num2) {
    // Cuando se ejecuta la condición, se ejecuta este código
    console.log('se puede retirar')
} else {
    // Cuando la condición no se cumple, se ejecuta este código
    console.log('no se puede retirar')
}

